                <p>The Gilman Scholarship is a grant for undergraduates who are deciding to study or intern abroad that are currently enrolled in a two-year or four-year college or university that struggle to fund their trip. Gilman encourages students to study languages, especially critical need languages which can be awarded. In all honesty, this scholarship is more of an opportunity to your future thanks to a large community of caring alumnus and the program itself. Gilman offers various workshops to help alumnus with their career. </p>
