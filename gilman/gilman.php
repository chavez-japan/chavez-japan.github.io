            <!-- Essays -->
            <section id="essays">
                <h3>Essays</h3>
                <p>There are two essays - Statement of Purpose and Follow-On Service Project Proposal.</p>
                <article>
                    <h4>Statement of Purpose</h4>
                    <p>This essay will introduce who you are and your study abroad or internship program. 
                    Remember, you cannot assume they know who you are so write down important highlights.</p>
                    <ul class="gilm-list">
                        <li><i class="fas fa-exclamation-circle"></i>Why study abroad? What led you to study abroad? <br>
                                NOTE: We all want to travel the world, so get more personal with this question. </li>
                        <li><i class="fas fa-exclamation-circle"></i>Describe the study or intern program.<br>
                                NOTE: Mention the impact of the program itself.</li>
                        <li><i class="fas fa-exclamation-circle"></i>What impacts will studying or interning abroad have to your academics and future goals?<br>
                                NOTE: You want to mention your weaknesses and how this program can improve them.</li>
                        <li><i class="fas fa-exclamation-circle"></i>What challenges, if any, will you have while being abroad? <br>
                                NOTE: Not only do you want to mention wherever you will be at but also the challenges that
                                are behind in your home state. </li>
                    </ul>
                </article>

                <article>
                    <h4>Follow-On Project Essay</h4>
                    <p>This essay will explain how you will inspire others to study or intern abroad. For example, this website is my follow-on project.</p>
                </article>

                <article>
                    <h4>Critical Need Language Award Essay</h4>
                    <p>If you are taking a language course while being abroad that is part of the Critical Need Language by the U.S. Department of State, then this optional essay is rewarded up to $8,000.</p>
                </article>
            </section>

